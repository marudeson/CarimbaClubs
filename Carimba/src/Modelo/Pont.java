package Modelo;

public class Pont {
	private int id;
	private String tAdv;
	private String carim;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTAdv() {
		return tAdv;
	}
	public void setTAdv(String tAdv) {
		this.tAdv = tAdv;
	}
	public String getCarim() {
		return carim;
	}
	public void setCarim(String carim) {
		this.carim = carim;
	}
	
}
